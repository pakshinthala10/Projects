friends = ["Kevin", "Karen", "Jim","Oscar", "Toby"]
#lists can have strings, numbers, or booleans

#access elements in the list
print(friends) #prints the whole list
print(friends[2]) #allows us to get the specific element that we want
print(friends[-3]) #allows us to access elements from the back
print(friends[1:3]) #allows us to access a specific values
print(friends[1:]) #will allow us to grab that first number and then everything after 

#modify elements in the list 
friends[1] = "Mike" #modify specific elements from the list
print(friends[1])

